<template>
  <div class="container py-5">
    <form class="form-group w-100" @submit.prevent.stop="handleSubmit">
      <div class="logo-container text-center">
        <img class="logo" src="/image/Logo.png" alt="Logo" />
      </div>
      <div class="brand-container text-center">
        <h1 class="brand h3">登入 Alphitter</h1>
      </div>
      <div class="form-label-group">
        <label for="account">帳號</label>
        <input
          id="account"
          v-model="account"
          name="account"
          type="account"
          class="form-input form-control"
          autocomplete="account"
          required
          autofocus
        />
      </div>

      <div class="form-label-group">
        <label for="password">密碼</label>
        <input
          id="password"
          v-model="password"
          name="password"
          type="password"
          class="form-input form-control"
          autocomplete="current-password"
          required
        />
      </div>

      <button
        class="btn-submit btn btn-lg btn-block mb-3"
        type="submit"
        :disabled="isProcessing"
      >
        登入
      </button>

      <div class="text-right d-flex justify-content-end mb-3">
        <p>
          <router-link to="/signup">註冊 Alphitter·</router-link>
        </p>
        <p>
          <router-link to="/admin/signin">後台登入</router-link>
        </p>
      </div>
    </form>
  </div>
</template>

<script>
import authorizationAPI from "./../apis/authorization";
import { Toast } from "./../utils/helpers";

export default {
  data() {
    return {
      account: "",
      email: "",
      password: "",
      isProcessing: false,
    };
  },
  methods: {
    async handleSubmit() {
      try {
        if (!this.account || !this.password) {
          Toast.fire({
            icon: "warning",
            title: "請填入 account 和 password",
          });
          return;
        }
        this.isProcessing = true;
        const response = await authorizationAPI.signIn({
          account: this.account,
          password: this.password,
        });
        const { data } = response;
        console.log(data);
        if (data.status !== "success") {
          throw new Error(data.message);
        }
        localStorage.setItem("token", data.token);

        this.$store.commit("setCurrentUser", data.user);

        this.$router.push("/main");
      } catch (error) {
        this.password = "";
        this.isProcessing = false;

        Toast.fire({
          icon: "warning",
          title: "請確認您輸入了正確的帳號密碼",
        });
      }
    },
  },
};
</script>

